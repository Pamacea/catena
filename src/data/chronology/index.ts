import { councils } from "../councils";
import { heresies } from "../heresies";

// ===== TYPES =====

export enum ChronologyEventType {
  COUNCIL = "COUNCIL",
  HERESY = "HERESY",
  BIBLE_BOOK = "BIBLE_BOOK",
  HISTORICAL = "HISTORICAL",
}

export interface ChronologyEvent {
  id: string;
  type: ChronologyEventType;
  year: number;
  yearEnd?: number;
  name: string;
  description: string;
  slug: string;
  href?: string;
  category?: string;
  tags?: string[];
  location?: string;
}

// ===== ADAPTERS =====

function adaptCouncils(): ChronologyEvent[] {
  return councils.map(c => ({
    id: `council-${c.slug}`,
    type: ChronologyEventType.COUNCIL as const,
    year: c.year,
    yearEnd: c.yearEnd,
    name: c.name,
    description: c.description,
    slug: c.slug,
    href: `/doctrine/councils/${c.slug}`,
    category: c.type,
    tags: c.topics,
    location: c.location,
  }));
}

function adaptHeresies(): ChronologyEvent[] {
  return heresies.map(h => ({
    id: `heresy-${h.slug}`,
    type: ChronologyEventType.HERESY as const,
    year: (h.century - 1) * 100 + 50,
    name: h.name,
    description: h.error,
    slug: h.slug,
    category: h.category,
    tags: h.keywords,
    location: h.origin,
  }));
}

