// ===== AGRÉGATION =====

export function getAllChronologyEvents(): ChronologyEvent[] {
  return [
    ...adaptCouncils(),
    ...adaptHeresies(),
    ...bibleBookDates,
    ...historicalEvents,
  ].sort((a, b) => a.year - b.year);
}

// ===== HELPERS =====

export function getEventsByType(type: ChronologyEventType): ChronologyEvent[] {
  return getAllChronologyEvents().filter(e => e.type === type);
}

export function formatYear(year: number): string {
  if (year <= 0) {
    return `~${Math.abs(year - 1)} av. J.-C.`;
  }
  return `~${year} apr. J.-C.`;
}

export function getEraLabel(year: number): string {
  if (year < -2000) return "Préhistoire biblique";
  if (year < -1200) return "Patriarches";
  if (year < -587) return "Rois et Prophètes";
  if (year < -4) return "Intertestamentaire";
  if (year < 100) return "Temps du Christ et Apôtres";
  if (year < 325) return "Église primitive";
  if (year < 476) return "Antiquité tardive";
  if (year < 1000) return "Haut Moyen Âge";
  if (year < 1500) return "Moyen Âge";
  if (year < 1800) return "Époque moderne";
  return "Époque contemporaine";
}

