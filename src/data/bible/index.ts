/**
 * Bible Data - Structure et Métadonnées
 *
 * Organisation des livres bibliques selon la Vulgate.
 */

export enum Testament {
  OLD = "OLD",
  NEW = "NEW",
}

export enum Genre {
  PENTATEUCH = "PENTATEUCH",
  HISTORICAL = "HISTORICAL",
  WISDOM = "WISDOM",
  PROPHETIC = "PROPHETIC",
  GOSPEL = "GOSPEL",
  ACTS = "ACTS",
  EPISTLE = "EPISTLE",
  APOCALYPTIC = "APOCALYPTIC",
}

export interface BibleBook {
  id: string;
  name: string;
  nameLatin: string;
  testament: Testament;
  genre: Genre;
  chapters: number;
  abbreviation: string;
  vulgateChapters?: number[];
}

export interface BibleChapter {
  bookId: string;
  chapter: number;
  verses: number;
  vulgateVerses?: number;
}

