export function searchDictionary(query: string): DictionaryEntry[] {
  const lowerQuery = query.toLowerCase()
  return [...theologicalConcepts, ...hebrewTerms, ...greekTerms].filter(
    (entry) =>
      entry.term.toLowerCase().includes(lowerQuery) ||
      entry.definition.toLowerCase().includes(lowerQuery) ||
      entry.biblicalReferences?.some((ref) => ref.toLowerCase().includes(lowerQuery)),
  )
}

export function getTermById(id: string): DictionaryEntry | undefined {
  return [...theologicalConcepts, ...hebrewTerms, ...greekTerms].find((t) => t.id === id)
}

export function getTermsByCategory(category: string): DictionaryEntry[] {
  return [...theologicalConcepts, ...hebrewTerms, ...greekTerms].filter((t) => t.category === category)
}

export function getTermsByLetter(letter: string): DictionaryEntry[] {
  return [...theologicalConcepts, ...hebrewTerms, ...greekTerms].filter((t) =>
    t.term.toLowerCase().startsWith(letter.toLowerCase()),
  )
}
