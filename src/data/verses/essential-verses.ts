function getBookId(frenchName: string): string {
  return bookIdMap[frenchName] || frenchName.toLowerCase().replace(/\s+/g, "-");
}

function parseReference(ref: string): { book: string; chapter: number; verse: number | string } {
  // Format: "Jean 3:16", "Genèse 1:1", "Matthieu 6:9-13"
  const match = ref.match(/^([^0-9]+)\s+(\d+):(\d+(?:-\d+)?)$/);
  if (!match) throw new Error(`Invalid reference: ${ref}`);

  return {
    book: match[1].trim(),
    chapter: parseInt(match[2], 10),
    verse: match[3].includes("-") ? match[3] : parseInt(match[3], 10),
  };
}

