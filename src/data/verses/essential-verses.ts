/**
 * Helpers
 */
export function getVersesByTheme(theme: VerseTheme): EssentialVerse[] {
  return essentialVerses.filter(v => v.theme === theme);
}

export function getVersesByBook(bookId: string): EssentialVerse[] {
  return essentialVerses.filter(v => v.bookId === bookId);
}

export function searchVerses(query: string): EssentialVerse[] {
  const lowerQuery = query.toLowerCase();
  return essentialVerses.filter(
    v =>
      v.text.toLowerCase().includes(lowerQuery) ||
      v.themeLabel.toLowerCase().includes(lowerQuery) ||
      v.reference.toLowerCase().includes(lowerQuery) ||
      v.tags.some(t => t.toLowerCase().includes(lowerQuery))
  );
}

export function getVerseById(id: string): EssentialVerse | undefined {
  return essentialVerses.find(v => v.id === id);
}

export { essentialVerses };
