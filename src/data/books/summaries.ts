// ===== EXPORTS =====

export function getSummaryById(bookId: string): BookSummary | undefined {
  return bookSummaries.find((s) => s.bookId === bookId)
}

export function getSummariesByTestament(testament: "OLD" | "NEW"): BookSummary[] {
  return bookSummaries.filter((s) => s.testament === testament)
}

export function getSummariesBySection(section: Section): BookSummary[] {
  return bookSummaries.filter((s) => s.section === section)
}

export function searchSummaries(query: string): BookSummary[] {
  const lowerQuery = query.toLowerCase()
  return bookSummaries.filter(
    (s) =>
      s.bookName?.toLowerCase().includes(lowerQuery) ||
      (typeof s.overview === 'string' ? s.overview.toLowerCase().includes(lowerQuery) : false) ||
      s.keyThemes?.some((t) => typeof t === 'string' ? t.toLowerCase().includes(lowerQuery) : t.theme?.toLowerCase().includes(lowerQuery)) ||
      s.keyFigures?.some((f) => typeof f === 'string' ? f.toLowerCase().includes(lowerQuery) : f.name?.toLowerCase().includes(lowerQuery)),
  )
}
